package Chat.ChatApp;

public class User {
    private String name;
    private String channelImOn;

    public User() {
        this.name = name;
        this.channelImOn = null;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setChannelImOn() {
        this.channelImOn = channelImOn;
    }

    public String getName() {
        return this.name;
    }
}